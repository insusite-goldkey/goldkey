def create_sample_files():
    """샘플 파일 생성 함수"""
    os.makedirs("rag_sample_docs", exist_ok=True)
    
    for filename, content in sample_documents.items():
        with open(f"rag_sample_docs/{filename}.txt", "w", encoding="utf-8") as f:
            f.write(content)
    
    print("RAG 시스템 테스트용 샘플 파일이 생성되었습니다.")
    print("생성된 파일:")
    for filename in sample_documents.keys():
        print(f"  - rag_sample_docs/{filename}.txt")

if __name__ == "__main__":
    create_sample_files()
