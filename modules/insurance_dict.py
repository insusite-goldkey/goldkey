def dict_search(query: str) -> list[dict]:
    """
    쿼리 문자열로 용어 사전 검색.
    1순위: term 키 완전 일치
    2순위: term·definition·tags·kcd 포함 검색
    반환: [{key, entry}, ...] 정렬된 결과 리스트
    """
    q = query.strip().lower()
    results = []
    seen = set()

    # 1순위: 키 직접 일치
    for key, entry in INSURANCE_DICT.items():
        if q == key.lower() or q == entry["term"].lower():
            if key not in seen:
                results.insert(0, {"key": key, "entry": entry, "score": 100})
                seen.add(key)

    # 2순위: 포함 검색 (term, definition, tags, kcd)
    for key, entry in INSURANCE_DICT.items():
        if key in seen:
            continue
        score = 0
        if q in entry["term"].lower():
            score += 60
        if q in entry["definition"].lower():
            score += 30
        if any(q in t.lower() for t in entry.get("tags", [])):
            score += 40
        if any(q in k.lower() for k in entry.get("kcd", [])):
            score += 35
        if score > 0:
            results.append({"key": key, "entry": entry, "score": score})
            seen.add(key)

    results.sort(key=lambda x: -x["score"])
    return results


def dict_by_category(category: str) -> list[dict]:
    """카테고리별 용어 목록 반환"""
    return [
        {"key": k, "entry": v}
        for k, v in INSURANCE_DICT.items()
        if v["category"] == category
    ]


